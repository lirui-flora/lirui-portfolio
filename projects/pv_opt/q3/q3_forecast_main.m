% 单位变动成本数据（1~8月）
months = (1:8)';

N1_cost = [17037.20; 15456.88; 13886.58; 13187.85; 13154.84; 11634.71; 10862.48; 9384.46];
N2_cost = [22579.22; 20482.13; 18409.65; 17478.06; 17430.43; 15422.39; 14402.71; 12448.94];
N3_cost = [19496.70; 17682.19; 15893.61; 15090.05; 15052.00; 13313.52; 12432.69; 10743.43];
P_cost  = [21738.23; 19419.40; 17312.99; 16516.10; 16473.22; 13703.19; 13664.81; 11502.47];

% 构建并拟合线性回归模型
X = [ones(length(months), 1), months]; % 添加常数项用于线性拟合

% 回归系数
b_N1 = X \ N1_cost;
b_N2 = X \ N2_cost;
b_N3 = X \ N3_cost;
b_P  = X \ P_cost;

% 预测9月（month = 9）的单位变动成本
month9 = [1, 9];

N1_pred = month9 * b_N1;
N2_pred = month9 * b_N2;
N3_pred = month9 * b_N3;
P_pred  = month9 * b_P;

% 显示结果
fprintf('九月份单位变动成本预测结果（单位：元）\n');
fprintf('N1: %.2f\n', N1_pred);
fprintf('N2: %.2f\n', N2_pred);
fprintf('N3: %.2f\n', N3_pred);
fprintf('P : %.2f\n', P_pred);